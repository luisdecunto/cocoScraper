def get_lang() -> str:
    return st.session_state.get("lang", "es")


def t(key: str, **kwargs: object) -> str:
    """Return translated string for current language, falling back to English."""
    lang = get_lang()
    value = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key, key)
    if kwargs:
        try:
            return value.format(**kwargs)
        except (KeyError, IndexError):
            return value
    return value
